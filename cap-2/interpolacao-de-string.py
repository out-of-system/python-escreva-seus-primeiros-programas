# em Python podemos interpolar strings usando % ou a função .format()

minha_str = "%d dias para a copa" %(100)
print(minha_str)
print("%d dias para a copa 2022" %(657))
print("faltam {} dias para a copa de 2022!".format(657))
print("ainda faltam {dias} para o Natal!".format(dias=310))
